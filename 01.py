'''
1. A imobiliária Imóbilis vende apenas terrenos retangulares. Faça um algoritmo para ler as
dimensões de um terreno e depois exibir a área do terreno.
'''

print("Olá, bem vindo ao sistema da imobiliária Imóbilis.")
print("Por favor informe as dimenções do seu terreno.")
Largura=int(input("Insira aqui a largura do seu terreno: "))
Comprimento=int(input("Insira aqui o comprimento do seu terreno: "))

Area=Largura*Comprimento

print("A area do seu terreno é:", Area)